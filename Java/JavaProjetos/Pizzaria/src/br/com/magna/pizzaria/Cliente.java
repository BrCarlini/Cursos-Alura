package br.com.magna.pizzaria;


public class Cliente {
	
	private String nome;
	private String telefone;
	
	
	public Cliente(String nome, String telefone) {
		this.nome = nome;
		this.telefone = telefone;
	}
	
	
	// Get de Nome
	
	public String getNome() {
		return nome;
	}
	

	
	// Get de Telefone
	
	public String getTelefone() {
		return telefone;
	}
	
	
	
	

}
